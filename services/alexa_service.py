def procesar_accion(accion):
    if accion == "mate":
        return "Entrando a matemáticas"
    
    elif accion == "lenguaje":
        return "Entrando a lenguaje"
    
    elif accion == "memoria":
        return "Ejercicios de memoria"
    
    elif accion == "musica":
        return "Entrando a música"

    return "No entendí"